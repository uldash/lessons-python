# Онлайн-школа BEEGEEK 5
# Каждый ученик, обучающийся в онлайн-школе BEEGEEK изучает либо 
# математику, либо информатику, либо оба этих предмета. У 
# руководителя школы есть списки учеников, изучающих каждый 
# предмет. Случайно списки всех учеников перемешались.

# Напишите программу, которая позволит руководителю выяснить, 
# сколько учеников изучает только один предмет.

# Формат входных данных
# На вход программе в первых двух строках подаются числа m и n – 
# количества учеников, изучающих математику и информатику 
# соответственно. Далее идут m+n строк — фамилии учеников, изучающих 
# математику и информатику, в произвольном порядке.

# Формат выходных данных
# Программа должна вывести количество учеников, которые изучают 
# только один предмет. Если таких учеников не окажется, то 
# необходимо вывести NO.

# Примечание. Гарантируется, что среди учеников школы BEEGEEK нет 
# однофамильцев.

m,n = int(input()),int(input())
fio = [input() for _ in range(n+m)]
res = n + m - 2 * (len(fio) - len(set(fio)))
print(res) if res != 0 else print('NO')